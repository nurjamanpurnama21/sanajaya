							<p> <div>
	<span style="text-align: justify;">Beberapa tahun belakangan obat herbal seperti menjadi tren di kalangan masyarakat Indonesia. Berbeda dengan obat kimia yang dianggap lebih cepat reaksinya, harus dengan resep dokter dan dapat menimbulkan efek samping, obat herbal dianggap lebih lama menyembuhkan, bisa dikonsumsi tanpa resep dokter dan tidak memiliki efek samping. Tetapi apakah benar seperti itu? Mari kita simak bersama penjelasan mengenai obat herbal.&nbsp;</span></div>
<div style="text-align: justify;">
	&nbsp;</div>
<div style="text-align: justify;">
	Obat herbal terbuat dari sumber yang berasal dari alam, yaitu tumbuh-tumbuhan. Pada dasarnya tanaman yang dijadikan obat herbal adalah tanaman yang telah digunakan sebagai ramuan jamu tradisional yang turun temurun sejak jaman leluhur kita. Bisa jadi tumbuhan yang dipilih ada di sekitar kita atau tumbuhan langka. Masyarakat di jaman dulu meracik obat herbal dalam bentuk jamu tradisional, karena di jaman itu belum ada peralatan medis yang canggih seperti sekarang. Lalu mengapa kita harus mengkonsumsi obat herbal yang terkesan tradisional di jaman yang kini sudah sangat modern?&nbsp;</div>
<div style="text-align: justify;">
	&nbsp;</div>
<div style="text-align: justify;">
	Obat herbal beradaptasi dengan tubuh&nbsp;</div>
<div style="text-align: justify;">
	Saat Anda sakit, dokter akan memberikan obat-obatan yang bersifat keras namun telah disesuaikan dosisnya. Resep yang selalu disertakan adalah paracetamol sebagai penahan rasa sakit dan antibiotikuntuk mengatasi infeksi bakteri. Biasanya obat-obatan keras menimbulkan rasa kantuk, dan tidak boleh dikonsumsi lebih dari tiga hari berturut-turut agar tidak memicu kerusakan ginjal. Ini dikarenakan tubuh kita yang mulai terbiasa dengan obat kimia, membentuk pertahanan pada partikel obat kimia yang awalnya terasa asing, sehingga apabila dikonsumsi jangka panjang lama-kelamaan Anda tidak merasakan manfaat apa-apa. Ginjal yang tugasnya menyaring darah dan menetralisir racun, menjadi kewalahan dan mengalami kerusakan dengan ditandai gejala: tubuh tidak mampu lagi menerima obat. Bukan berarti Anda tidak boleh mengkonsumsi obat kimia, namun patuhilah anjuran dokter dan jangan melebihi batas waktu.&nbsp;</div>
<div style="text-align: justify;">
	&nbsp;</div>
<div style="text-align: justify;">
	Obat herbal bekerja langsung ke sumbernya&nbsp;</div>
<div style="text-align: justify;">
	Karena berasal dari alam, sifatnya yang alami membuat obat herbal memiliki partikel yang dapat beradaptasi dengan tubuh manusia. Contoh yang bisa diambil adalah senyawa acetogenin pada daun sirsak. Acetogenin ketika dimasukkan ke dalam tubuh manusia yang mengidap kanker, dapat mendeteksi adanya perkembangbiakan sel yang tidak wajar. Acetogenin bergerak langsung menuju sel abnormal, tanpa membahayakan sel yang masih sehat. Ia menghambat perkembangan sel abnormal, sehinga sel abnormal tersebut gagal berkembang biak dan mati. Itulah alasan kenapa orang di jaman dulu menggunakan air rebusan daun sirsak untuk menyembuhkan kanker.&nbsp;</div>
<div style="text-align: justify;">
	&nbsp;</div>
<div style="text-align: justify;">
	Bisakah kita meracik obat herbal sendiri?&nbsp;</div>
<div style="text-align: justify;">
	Beberapa bahan alami herbal memang bisa ditemukan di lingkungan sekitar kita, namun perlu diperhatikan apakah bahan tersebut mengandung senyawa-senyawa yang merugikan tubuh. Misalnya; Anda bisa menemukan daun sirsak dengan mudahnya, tapi tidak menjamin Anda dapat mensterilkan daun sirsak tersebut dengan cara yang sama seperti para ahli obat. Pada jaman dahulu, lingkungan masih murni, belum begitu tercemar. Jadi setiap orang bisa meracik sendiri obat dengan cara tradisional asalkan diberi pengetahuan. Sedangkan jaman sekarang, polusi dimana-mana, hutan semakin berkurang, kecil sekali kemungkinan kita untuk mendapatkan bahan-bahan alami yang belum tercemar. Kecuali Anda tinggal di pedesaan terpencil. Disinilah para ahli obat berperan, mereka menggunakan teknologi dan pengetahuan untuk mensterilkan bahan-bahan tersebut, mengumpulkan manfaatnya, dan menyajikannya dalam bentuk obat ke hadapan Anda.&nbsp;</div>
<div style="text-align: justify;">
	&nbsp;</div>
<div style="text-align: justify;">
